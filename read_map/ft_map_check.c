#include "../so_long.h"

void	ft_map_check(t_prgrm *vars, char *map)
{
	char	*i;

	i = ft_strchr(map, '.');
	if (!(i[0] == '.' && i[1] == 'b' && i[2] == 'e' && i[3] == 'r' &&
		i[4] == '\0'))
		ft_error(vars, 6);
}
