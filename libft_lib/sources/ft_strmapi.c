#include "../libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				s_len;
	char			*new;

	if (!s)
		return (NULL);
	i = 0;
	s_len = ft_strlen(s);
	new = malloc(1 * s_len + 1);
	if (new == 0)
		return (NULL);
	while (s[i] != '\0')
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
