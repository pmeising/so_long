#include "../libft.h"

t_list	*ft_lstnew(void *content)
{
	struct s_list	*newnode;

	newnode = malloc(sizeof(struct s_list));
	if (!newnode)
		return (NULL);
	newnode->content = NULL;
	newnode->content = content;
	newnode -> next = NULL;
	return (newnode);
}
