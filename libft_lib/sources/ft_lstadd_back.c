#include "../libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*ptr_to_head;

	ptr_to_head = *lst;
	if (*lst == NULL)
		*lst = new;
	else
	{
		while (ptr_to_head->next != NULL)
			ptr_to_head = ptr_to_head->next;
		ptr_to_head->next = new;
	}
}
